package day01; // 현재 클래스파일이 위치한 패키지/폴더

public class Step2 { // class start // 모든 코드는 class 안에서만 작성한다
    // main + 엔터 => 함수 // 코드 읽어주는 역할(스레드) 포함
    public static void main(String[] args) { // main start
        int level; // 정수(소수점없는 숫자)형 'level'이름으로 변수 선언
        level = 10; // level 변수에 10 대입
        System.out.println(level); // 변수 호출하고 콘솔에 출력
        System.out.println("level"); // 변수 호출하고 콘솔에 출력
        int level2 = 10; // 정수형 'level2' 이름으로 변수 선언하면서 10대입
        System.out.println(level2);
    } // main end
} // class end
